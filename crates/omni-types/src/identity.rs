// =============================================================================
// UUID-backed identifiers (AgentId, CapabilityId, SessionId).
// =============================================================================
//
// These three identifiers are all UUIDv7 — sortable, time-ordered, 128 bit.
// We define them as distinct newtypes so the compiler enforces the
// distinction (`fn revoke(cap: CapabilityId)` cannot accidentally accept a
// `SessionId`). The shared backing type is hidden as an implementation
// detail.
//
// UUIDv7 was chosen over v4 because its leading 48 bits are a
// millisecond-precision timestamp, which gives:
//   1. Better cache locality in B-tree-backed stores (e.g., the
//      revocation list in `omni-capability`).
//   2. A natural "issued at" hint for debugging without an extra field.
//
// We do not rely on the timestamp for security. The capability token
// carries an explicit `not_before` / `not_after` window (see P1.3.a).

/// Local identifier of an AI agent within a node.
///
/// Generated when the agent starts. Lifetime is bound to the agent's
/// process; reusing an `AgentId` after process exit is not allowed.
///
/// `Zeroize` is implemented manually (not derived) because `uuid::Uuid`
/// does not implement `Zeroize`. The manual implementation overwrites
/// the inner UUID with the nil UUID (all zeroes), achieving the same
/// invariant as the derive macro would for a `[u8; 16]` field.
#[derive(Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, Serialize, Deserialize)]
pub struct AgentId(Uuid);

impl Zeroize for AgentId {
    fn zeroize(&mut self) {
        self.0 = Uuid::nil();
    }
}

impl AgentId {
    /// Length of the underlying byte representation, in bytes.
    pub const LEN: usize = 16;

    /// Generate a fresh `UUIDv4` identifier.
    ///
    /// Requires a working CSPRNG (`getrandom`). Panics in environments
    /// without a randomness source — these are not supported targets.
    ///
    /// # Panics
    ///
    /// Panics if `getrandom` cannot produce entropy on the current platform.
    /// On Linux this means `getrandom(2)` returned an unrecoverable error.
    /// The panic message is part of the stable contract; do not change
    /// without bumping a major version.
    #[must_use]
    pub fn new() -> Self {
        Self(Uuid::from_bytes(random_uuid_bytes()))
    }

    /// Construct from raw bytes (deserialization path).
    #[must_use]
    pub const fn from_bytes(bytes: [u8; 16]) -> Self {
        Self(Uuid::from_bytes(bytes))
    }

    /// Borrow the underlying bytes.
    #[must_use]
    pub fn as_bytes(&self) -> &[u8; 16] {
        self.0.as_bytes()
    }
}

impl fmt::Debug for AgentId {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        fmt_truncated("AgentId", self.0.as_bytes(), f)
    }
}

impl IdHex for AgentId {
    fn to_hex(&self) -> String {
        hex::encode(self.0.as_bytes())
    }

    fn from_hex(s: &str) -> Option<Self> {
        let mut buf = [0u8; 16];
        hex::decode_to_slice(s, &mut buf).ok()?;
        Some(Self(Uuid::from_bytes(buf)))
    }
}

