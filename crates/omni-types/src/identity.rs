#[cfg(test)]
mod tests {
    use super::*;
    use proptest::prelude::*;

    // -------------------------------------------------------------------------
    // Round-trip tests: byte-array -> ID -> byte-array.
    // -------------------------------------------------------------------------

    #[test]
    fn node_id_round_trip_bytes() {
        let bytes = [42u8; 32];
        let id = NodeId::from_attestation_hash(bytes);
        assert_eq!(id.as_bytes(), &bytes);
    }

    #[test]
    fn model_id_round_trip_bytes() {
        let bytes = [7u8; 32];
        let id = ModelId::from_manifest_hash(bytes);
        assert_eq!(id.as_bytes(), &bytes);
    }

    // -------------------------------------------------------------------------
    // Hex round-trip.
    // -------------------------------------------------------------------------

    #[test]
    fn node_id_hex_round_trip() {
        let original = NodeId::from_attestation_hash([0xAB; 32]);
        let hex = original.to_hex();
        // 32 bytes = 64 hex chars.
        assert_eq!(hex.len(), 64);
        let decoded = NodeId::from_hex(&hex).expect("valid hex round-trip");
        assert_eq!(original, decoded);
    }

    #[test]
    fn capability_id_hex_round_trip() {
        let original = CapabilityId::new();
        let hex = original.to_hex();
        assert_eq!(hex.len(), 32); // 16 bytes = 32 hex chars
        let decoded = CapabilityId::from_hex(&hex).expect("valid hex round-trip");
        assert_eq!(original, decoded);
    }

    // -------------------------------------------------------------------------
    // `from_hex` does not panic on garbage input.
    // -------------------------------------------------------------------------

    #[test]
    fn node_id_from_hex_rejects_wrong_length() {
        assert!(NodeId::from_hex("too short").is_none());
        assert!(NodeId::from_hex("").is_none());
    }

    #[test]
    fn node_id_from_hex_rejects_invalid_chars() {
        // 64 chars but with a non-hex character. Using `format!` keeps us
        // inside the `core::fmt` machinery without needing `ToString` in scope
        // (we are `no_std + alloc`, so `ToString` is not in the default
        // prelude).
        let bad = alloc::format!("ZZ{}", "00".repeat(31));
        assert!(NodeId::from_hex(&bad).is_none());
    }

    // -------------------------------------------------------------------------
    // UUIDv7-based IDs are unique across calls (very high probability).
    // -------------------------------------------------------------------------

    #[test]
    fn agent_id_new_is_unique() {
        let a = AgentId::new();
        let b = AgentId::new();
        // Probability of collision in two consecutive UUIDv7s is ~2^-74 due
        // to the random component; assertion is statistically safe.
        assert_ne!(a, b);
    }

    #[test]
    fn capability_id_new_is_unique() {
        let a = CapabilityId::new();
        let b = CapabilityId::new();
        assert_ne!(a, b);
    }

    #[test]
    fn session_id_new_is_unique() {
        let a = SessionId::new();
        let b = SessionId::new();
        assert_ne!(a, b);
    }

    // -------------------------------------------------------------------------
    // Debug output is bounded and never reveals the full identifier.
    // -------------------------------------------------------------------------

    #[test]
    fn debug_output_is_truncated() {
        let id = NodeId::from_attestation_hash([0xFF; 32]);
        let dbg = alloc::format!("{id:?}");
        // "NodeId(ffffffff…)" is 18 chars; full hex would be 70+. Assert
        // bounded — this is the security invariant we care about.
        assert!(
            dbg.len() < 30,
            "debug output unexpectedly long: {dbg} ({})",
            dbg.len()
        );
        assert!(dbg.contains("NodeId"));
        assert!(dbg.contains('…'));
    }

    // -------------------------------------------------------------------------
    // Determinism: identical inputs -> identical IDs.
    // -------------------------------------------------------------------------

    proptest! {
        #[test]
        fn node_id_determinism(bytes in proptest::array::uniform32(any::<u8>())) {
            let a = NodeId::from_attestation_hash(bytes);
            let b = NodeId::from_attestation_hash(bytes);
            prop_assert_eq!(a, b);
        }

        #[test]
        fn model_id_determinism(bytes in proptest::array::uniform32(any::<u8>())) {
            let a = ModelId::from_manifest_hash(bytes);
            let b = ModelId::from_manifest_hash(bytes);
            prop_assert_eq!(a, b);
        }

        // Distinct inputs -> distinct IDs (collision-free in 32 bytes is
        // a property of the underlying hash, but here we test the wrapper:
        // it must not lose entropy by truncation or normalisation).
        #[test]
        fn node_id_distinct_inputs_distinct_ids(
            a_bytes in proptest::array::uniform32(any::<u8>()),
            b_bytes in proptest::array::uniform32(any::<u8>()),
        ) {
            prop_assume!(a_bytes != b_bytes);
            let a = NodeId::from_attestation_hash(a_bytes);
            let b = NodeId::from_attestation_hash(b_bytes);
            prop_assert_ne!(a, b);
        }

        // Hex round-trip is total over all 32-byte inputs.
        #[test]
        fn node_id_hex_round_trip_total(bytes in proptest::array::uniform32(any::<u8>())) {
            let id = NodeId::from_attestation_hash(bytes);
            let hex = id.to_hex();
            let decoded = NodeId::from_hex(&hex).expect("round-trip cannot fail");
            prop_assert_eq!(id, decoded);
        }

        #[test]
        fn model_id_hex_round_trip_total(bytes in proptest::array::uniform32(any::<u8>())) {
            let id = ModelId::from_manifest_hash(bytes);
            let hex = id.to_hex();
            let decoded = ModelId::from_hex(&hex).expect("round-trip cannot fail");
            prop_assert_eq!(id, decoded);
        }
    }

    // -------------------------------------------------------------------------
    // Ordering: derived `Ord` is consistent with byte-level ordering.
    // -------------------------------------------------------------------------

    #[test]
    fn node_id_ordering_matches_byte_order() {
        let smaller = NodeId::from_attestation_hash([0x00; 32]);
        let larger = NodeId::from_attestation_hash([0xFF; 32]);
        assert!(smaller < larger);
    }
}
