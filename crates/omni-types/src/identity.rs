//! System-level identifiers.
//!
//! This module defines the strongly-typed identifier newtypes used across
//! OMNI OS. Every identifier kind has its own type so the compiler rejects
//! accidental conflation (e.g., passing a [`ModelId`] where a [`NodeId`] is
//! expected).
//!
//! # Identifier zoo
//!
//! | Type | Backing | Source of truth | Lifetime |
//! |---|---|---|---|
//! | [`NodeId`]       | `[u8; 32]` | TEE attestation report hash (BLAKE3) | Persistent — re-derivable from quote |
//! | [`ModelId`]      | `[u8; 32]` | Content-addressed hash of signed model manifest | Persistent |
//! | [`AgentId`]      | `Uuid` (v7) | Generated by the host node when an agent starts | Process-scoped |
//! | [`CapabilityId`] | `Uuid` (v7) | Generated by the capability issuer | Token-TTL-scoped |
//! | [`SessionId`]    | `Uuid` (v7) | Generated by the runtime when an inference session opens | Session-scoped |
//!
//! # Why no `core::fmt::Display`?
//!
//! These identifiers can transit through logs, traces, and metrics. A naive
//! `format!("{}", node_id)` would dump 32 bytes of attestation hash into a
//! log file, which is both unreadable and an audit liability. We omit
//! `Display` entirely. To surface an identifier explicitly, callers must
//! invoke [`IdHex::to_hex`], which is `grep`-friendly and reviewable.
//!
//! `Debug` is implemented but renders a truncated hex prefix so that
//! `tracing::debug!(?node_id)` produces a useful but bounded view.
//!
//! # Construction trust boundaries
//!
//! Each identifier exposes a constructor whose name documents where the
//! input must come from. For example, [`NodeId::from_attestation_hash`]
//! takes the BLAKE3 digest of a TEE quote — the caller is responsible for
//! having verified the quote first. Constructors that accept arbitrary
//! bytes (e.g., for deserialization) are namespaced (`from_bytes`) so
//! audit grep can find them.
//!
//! # `no_std` notes
//!
//! All types are `no_std + alloc`-compatible. Hex encoding uses [`alloc`]
//! buffers; no allocation is performed for equality / ordering.

use alloc::string::String;
use alloc::vec::Vec;
use core::fmt;

use serde::{Deserialize, Serialize};
use uuid::Uuid;
use zeroize::Zeroize;

// Helper used by AgentId / CapabilityId / SessionId to produce 16 random
// bytes from the platform CSPRNG. We invoke `getrandom` directly rather
// than going through `uuid`'s built-in `new_v4` so we can stay
// `no_std`-only and depend on a single entropy source for the workspace.
//
// Failure to obtain entropy is unrecoverable on a system that needs
// authentication / capability tokens to function — we panic with a
// stable message rather than swallow the error and emit predictable IDs.
fn random_uuid_bytes() -> [u8; 16] {
    let mut bytes = [0u8; 16];
    // Panic message is the stable contract: tests and probes can match on
    // it. Do not change without bumping a major version.
    //
    // `expect_used` is an intentional escape hatch here: a CSPRNG failure
    // is unrecoverable for OMNI OS (capability tokens, signatures, and
    // attestation all hinge on entropy). Returning a `Result` from this
    // helper would force every identifier constructor in the workspace
    // to be fallible, which is worse ergonomically and does not improve
    // the worst-case behaviour — the caller's only sensible response to
    // entropy failure is to abort.
    #[allow(clippy::expect_used)]
    getrandom::getrandom(&mut bytes)
        .expect("OMNI: CSPRNG (getrandom) failed — system entropy is unavailable");
    // Set the four most significant bits of byte 6 to 0100 (UUID version 4).
    // Set the two most significant bits of byte 8 to 10 (RFC 4122 variant).
    // This produces a valid UUIDv4 layout from arbitrary random bytes —
    // the same bit-twiddle that `uuid`'s `new_v4()` performs internally.
    // `indexing_slicing` is allowed because the indices 6 and 8 are
    // compile-time constants strictly less than the array length 16.
    #[allow(clippy::indexing_slicing)]
    {
        bytes[6] = (bytes[6] & 0x0F) | 0x40;
        bytes[8] = (bytes[8] & 0x3F) | 0x80;
    }
    bytes
}

