/// Helper trait implemented by every identifier in this module.
///
/// Centralises the conversion to/from hex strings so callers always go
/// through an auditable code path when an identifier needs to leave the
/// process boundary (logs, metrics, wire formats meant for humans).
pub trait IdHex: Sized {
    /// Encode this identifier as a lowercase hex string. Allocates.
    fn to_hex(&self) -> String;

    /// Decode an identifier from a hex string. Returns `None` on length
    /// mismatch or invalid hex digits — never panics.
    fn from_hex(s: &str) -> Option<Self>;
}

// Internal helper: render a fixed-size byte array as a Debug-friendly
// `Type(<8-hex-prefix>…)` string. We intentionally truncate so
// `tracing::debug!(?node_id)` does not flood logs with full digests.
//
// SECURITY: never widen this prefix without auditing every log site that
// carries identifiers. The 8-char prefix is short enough to remain
// non-correlatable across logs but long enough to disambiguate within a
// single trace (~2^32 distinct prefixes).
fn fmt_truncated(name: &str, bytes: &[u8], f: &mut fmt::Formatter<'_>) -> fmt::Result {
    write!(f, "{name}(")?;
    // Render the first 4 bytes (= 8 hex chars). Manual loop avoids pulling
    // in `core::fmt::Write::write_fmt`'s formatter machinery for byte
    // slices, which is awkward with the `hex` crate's no_std API.
    for b in bytes.iter().take(4) {
        write!(f, "{b:02x}")?;
    }
    write!(f, "…)")
}

// =============================================================================
// NodeId — TEE-attested hardware identity.
// =============================================================================

/// Globally unique identifier of an OMNI OS node.
///
/// A `NodeId` is the BLAKE3 digest of the node's TEE attestation quote.
/// It is therefore:
///
/// * **Deterministic** — re-deriving the hash from the same quote yields
///   the same `NodeId`.
/// * **Unforgeable** — minting a `NodeId` for hardware you do not own
///   requires either compromising the TEE root of trust or finding a
///   BLAKE3 collision (assumed infeasible).
/// * **Stable across reboots** — the quote's measurement covers the
///   firmware identity, not session state.
///
/// # Construction
///
/// In production, `NodeId` is constructed via [`NodeId::from_attestation_hash`]
/// after the caller has verified a TEE quote (Phase 1 = `omni-tee` — see
/// `/todo.md` P5). The [`NodeId::from_bytes`] constructor exists for
/// deserialization paths only and should not be used to manufacture node
/// identities at runtime.
#[derive(Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, Serialize, Deserialize, Zeroize)]
pub struct NodeId([u8; 32]);

impl NodeId {
    /// Length of the underlying byte representation, in bytes.
    pub const LEN: usize = 32;

    /// Construct a `NodeId` from the BLAKE3 hash of a verified TEE
    /// attestation quote.
    ///
    /// The caller is responsible for having verified the quote first
    /// (P5 — `omni-tee`). Passing an unverified hash bypasses the trust
    /// boundary and is a misuse of this API.
    #[must_use]
    pub const fn from_attestation_hash(hash: [u8; 32]) -> Self {
        Self(hash)
    }

    /// Construct a `NodeId` from raw bytes.
    ///
    /// Reserved for deserialization paths. Production code should prefer
    /// [`NodeId::from_attestation_hash`] which documents the trust origin.
    #[must_use]
    pub const fn from_bytes(bytes: [u8; 32]) -> Self {
        Self(bytes)
    }

    /// Borrow the underlying bytes.
    #[must_use]
    pub const fn as_bytes(&self) -> &[u8; 32] {
        &self.0
    }
}

impl fmt::Debug for NodeId {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        fmt_truncated("NodeId", &self.0, f)
    }
}

impl IdHex for NodeId {
    fn to_hex(&self) -> String {
        hex::encode(self.0)
    }

    fn from_hex(s: &str) -> Option<Self> {
        let mut buf = [0u8; 32];
        hex::decode_to_slice(s, &mut buf).ok()?;
        Some(Self(buf))
    }
}

