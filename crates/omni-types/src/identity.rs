/// Convert any byte slice to lowercase hex. Allocates.
///
/// Public so other crates in the workspace can use the same convention
/// without re-importing `hex` directly.
#[must_use]
pub fn bytes_to_hex(bytes: &[u8]) -> String {
    hex::encode(bytes)
}

/// Decode a hex string into a freshly-allocated byte vector.
///
/// Returns `None` on invalid hex digits; never panics.
#[must_use]
pub fn hex_to_bytes(s: &str) -> Option<Vec<u8>> {
    hex::decode(s).ok()
}

// =============================================================================
// Tests
